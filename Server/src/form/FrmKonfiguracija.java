    private void popuniFormu() {
    try {
       
        Properties p = new Properties();
        p.load(new FileInputStream("db.properties"));
        String url= p.getProperty("url");
        String username= p.getProperty("user");
        String password=p.getProperty("pass");
        //jdbc:mysql://localhost:3306/soft_dev_d4
        String[] niz= url.split("/");
        String []iseckano= niz[2].split(":");
        String localhost=iseckano[0];
        String port=iseckano[1];
        String baza= niz[3];
        txtBaza.setText(baza);
        txtUsername.setText(username);
        txtPassword.setText(password);
        txtPort.setText(port);
        txtURL.setText(localhost);
    } catch (IOException ex) {
        JOptionPane.showMessageDialog(this,"Lose citanje iz properti fajla");
        Logger.getLogger(FrmKonfiguracija.class.getName()).log(Level.SEVERE, null, ex);
    }
       
    }

    

    
