    private void btnPotvrdiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPotvrdiActionPerformed
        String baza= txtBaza.getText().trim();
        String host= txtURL.getText().trim();
        String username=txtUsername.getText().trim();
        String password= new String(txtPassword.getPassword());
        String port= txtPort.getText().trim();
        if(host.isEmpty() || username.isEmpty() || port.isEmpty()){
            JOptionPane.showMessageDialog(this,"Host, username i port ne mogu biti prazni");
        }else if(!host.equals("localhost") || !username.equals("root")|| !port.equals("3306")){
        JOptionPane.showMessageDialog(this,"Proverite da li ste uneli ispravne podatke za konekciju sa bazom");
        
        }else{
           
            try {
                url="jdbc:mysql://"+host+":"+port+"/"+baza;
               
                Properties p = new Properties();
                p.setProperty("url", url);
                p.setProperty("user", username);
                p.setProperty("pass", password);
                p.store(new FileOutputStream("db.properties"),"properties");
                JOptionPane.showMessageDialog(this, "Konektovali ste se na bazu sa url-om: " +url);
                
                this.dispose();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(FrmKonfiguracija.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                System.out.println("Greska kod konfiguracije baze");
                Logger.getLogger(FrmKonfiguracija.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }
    }//GEN-LAST:event_btnPotvrdiActionPerformed

