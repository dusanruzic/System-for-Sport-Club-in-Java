    private void popuniPolja() {
        txtIme.setText(trener.getIme());
        txtPrezime.setText(trener.getPrezime());
        txtPlata.setText(trener.getPlata() + "");
        if (trener.getDaLiJeAktivanTrener()) {
            chkAktivan.setSelected(true);
        } else {
            chkAktivan.setSelected(false);
        }
    }
