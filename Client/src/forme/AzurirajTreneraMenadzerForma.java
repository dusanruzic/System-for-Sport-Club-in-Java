    private void btnIzmeniTreneraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniTreneraActionPerformed
        
        double plata;
        try {
           plata = Double.parseDouble(txtPlata.getText());
           if(plata < 0){
               throw new Exception();
           }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Unesi ispravnu platu!");
            return;

        }
        boolean izmenjeno = false;
        
        if(!txtIme.getText().isEmpty() && !txtPrezime.getText().isEmpty()){
            try {
            trener.setIme(txtIme.getText().trim());
            trener.setPrezime(txtPrezime.getText().trim());
            trener.setPlata(Double.parseDouble(txtPlata.getText().trim()));
            trener.setDaLiJeAktivanTrener(chkAktivan.isSelected());
            izmenjeno = Controler.getInstanca().azurirajTrenera(trener);
            JOptionPane.showMessageDialog(this, "Trener je uspesno izmenjen!");
        } catch (Exception ex) {
            Logger.getLogger(AzurirajTimMenadzerForma.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Trener nije uspesno izmenjen!");
        }
        }
        else {
            JOptionPane.showMessageDialog(this, "Unesi sve podatke!");

        }
        
    }//GEN-LAST:event_btnIzmeniTreneraActionPerformed


