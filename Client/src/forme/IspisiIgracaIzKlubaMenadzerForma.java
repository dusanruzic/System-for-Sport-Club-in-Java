    private void srediTabelu() {
        Sport sport = (Sport) cmbSport.getSelectedItem();
        String prezime = txtPrezime.getText().trim();

        Igrac igrac = new Igrac();
        igrac.setPozicija(new Pozicija());
        igrac.getPozicija().setSport(sport);
        igrac.setPrezime(prezime);
        ArrayList<Igrac> listaIgraca = new ArrayList<>();
        try {
            listaIgraca = Controler.getInstanca().pronadjiIgrace((Igrac) igrac);
        } catch (Exception ex) {
            Logger.getLogger(IspisiIgracaIzKlubaMenadzerForma.class.getName()).log(Level.SEVERE, null, ex);
        }
        ModelTabeleIgraci mti = new ModelTabeleIgraci(listaIgraca);
        tabelaIgraci.setModel(mti);
    }

    private void napuniKomboSportova() {
        ArrayList<Sport> listaSportova = new ArrayList<>();
        cmbSport.removeAllItems();
        try {
            listaSportova = Controler.getInstanca().vratiSveSportove();
        } catch (Exception ex) {
            Logger.getLogger(KreirajNoviTimMenadzerForma.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println(listaSportova);
        for (IGeneralEntity sport : listaSportova) {
            cmbSport.addItem(sport);
        }
    }
