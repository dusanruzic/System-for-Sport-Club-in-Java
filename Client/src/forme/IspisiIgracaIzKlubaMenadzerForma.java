    private void btnPrimeniFiltereActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrimeniFiltereActionPerformed
        Sport sport = (Sport) cmbSport.getSelectedItem();
        String prezime = txtPrezime.getText().trim();

        Igrac igrac = new Igrac();
        igrac.setPozicija(new Pozicija());
        igrac.getPozicija().setSport(sport);
        igrac.setPrezime(prezime);
        //igrac.setDatumRodjenja(new Date());
        ArrayList<Igrac> igraci = new ArrayList<>();
        try {
            igraci = Controler.getInstanca().pronadjiIgrace((igrac));
        } catch (Exception ex) {
            Logger.getLogger(IspisiIgracaIzKlubaMenadzerForma.class.getName()).log(Level.SEVERE, null, ex);
        }
        ModelTabeleIgraci mti = (ModelTabeleIgraci) tabelaIgraci.getModel();
        mti.setListaIgraca(igraci);
        mti.fireTableDataChanged();
        
        if(igraci.size() >= 1) {
            JOptionPane.showMessageDialog(this, "Sistem je nasao igrace po zadatoj vrednosti");


        }
        else {
            JOptionPane.showMessageDialog(this, "Sistem nije nasao igrace po zadatoj vrednosti");

        }
    }//GEN-LAST:event_btnPrimeniFiltereActionPerformed

