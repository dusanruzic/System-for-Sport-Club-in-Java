    private void btnUpisiIgracaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpisiIgracaActionPerformed

        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            String ime = txtIme.getText();
            String prezime = txtPrezime.getText();
            String JMBG = txtJMBG.getText();
            double plata = Double.parseDouble(txtPlata.getText());
            boolean daLiJeKapiten = chbDaLiJeKapiten.isSelected();
            Date datum = sdf.parse(txtDatumRodjenja.getText());
            Tim tim = (Tim) cmbTim.getSelectedItem();
            Pozicija pozicija = (Pozicija) cmbPozicija.getSelectedItem();

            if (!ime.isEmpty() && !prezime.isEmpty() && !JMBG.isEmpty() && plata > 0) {
                
                if(JMBG.length() != 13){
                   JOptionPane.showMessageDialog(this, "JMBG mora imati tacno 13 cifara!");
                   return;

                }
                int redniBroj = 0;
                try {
                    redniBroj = Controler.getInstanca().poslednjiRBIgraca();
                } catch (Exception ex) {
                    Logger.getLogger(UpisiIgracaUKlubMenadzerForma.class.getName()).log(Level.SEVERE, null, ex);
                }

                Igrac igrac = new Igrac(redniBroj, ime, prezime, JMBG, plata, daLiJeKapiten, datum, true, tim, pozicija);
                Igrac i = null;
                try {

                    i = Controler.getInstanca().zapamtiIgraca(igrac);
                    System.out.println("igrac: " + i);
                } catch (Exception ex) {
                    Logger.getLogger(KreirajNoviTimMenadzerForma.class.getName()).log(Level.SEVERE, null, ex);
                }
                if (i != null) {
                    ModelTabeleIgraci mtigraci = (ModelTabeleIgraci) tblIgraci.getModel();
                    mtigraci.dodajIgraca(i);
                    JOptionPane.showMessageDialog(this, "Sistem je uspesno zapamtio novog igraca!");

                    btnUpisiIgraca.setEnabled(false);
                    btnKreiraj.setEnabled(true);
                    btnOdustani.setEnabled(false);

                    txtIme.setText("");
                    txtPrezime.setText("");
                    txtJMBG.setText("");
                    txtPlata.setText("");
                    txtDatumRodjenja.setText("");

                } else {
                    JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti novog igraca");
                }

            } else {
                JOptionPane.showMessageDialog(this, "Unesi sve podatke");

            }

        } catch (ParseException ex) {
            Logger.getLogger(UpisiIgracaUKlubMenadzerForma.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Unesi datum u ispravnom formatu (dd.mm.yyyy)");
        } catch (NumberFormatException nex) {
            Logger.getLogger(UpisiIgracaUKlubMenadzerForma.class.getName()).log(Level.SEVERE, null, nex);
            JOptionPane.showMessageDialog(this, "Plata mora biti pozitivna!");
        }
    }//GEN-LAST:event_btnUpisiIgracaActionPerformed

