    private void popuniKomboTim() {
        ArrayList<Tim> listaTimova = new ArrayList<>();
        try {
            listaTimova = Controler.getInstanca().vratiSveTimove();
        } catch (Exception ex) {
            Logger.getLogger(UpisiIgracaUKlubMenadzerForma.class.getName()).log(Level.SEVERE, null, ex);
        }
        cmbTim.removeAllItems();
        for (Tim tim : listaTimova) {
            cmbTim.addItem(tim);
        }
    }

    private void popuniKomboPozicija() {
        Tim t = (Tim) cmbTim.getSelectedItem();
        if (t != null) {
            ArrayList<Pozicija> listaPozicija = new ArrayList<>();
            try {
                listaPozicija = Controler.getInstanca().vratiPozicijeSporta(t.getTrener().getSport());
            } catch (Exception ex) {
                Logger.getLogger(UpisiIgracaUKlubMenadzerForma.class.getName()).log(Level.SEVERE, null, ex);
            }
            cmbPozicija.removeAllItems();
            for (Pozicija pozicija : listaPozicija) {
                cmbPozicija.addItem(pozicija);
            }
        }

    }

    private void popuniTabeluIgraci() {
        ModelTabeleIgraci mti = new ModelTabeleIgraci();
        tblIgraci.setModel(mti);
    }
